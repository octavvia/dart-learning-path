void main() {
  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second); // pembagian dengan hasil double
  print(first ~/
      second); //pembagian dengan hasil pembulatan kebawah (hasilnya int)
  print(first % second); // sisa bagi
}
