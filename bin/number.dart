void main() {
  int number1 = 10;
  double number2 = 3.14;

  print(number1);
  print(number2);

  // penggunaan num -> tipe data yang isinya bisa diganti-ganti
  num number3 = 10;
  number3 = 3.14;
  print(number3);

  // boolean
  bool isTrue = true;
  print(isTrue);
}
/* di flutter terdapat dua tipe data number yaitu int dan double
int -> bilangan bulat
double -> bilangan desimal

num -> tipe data yang isinya bisa berupa int atau double (bisa diganti-ganti)
*/